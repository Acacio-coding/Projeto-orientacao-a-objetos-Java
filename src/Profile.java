public class Profile{
    String name;
    String birth;
    String gender;

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getBirth(){
        return birth;
    }

    public void setBirth(String birth){
        this.birth = birth;
    }

    public String getGender(){
        return gender;
    }

    public void setGender(String gender){
        this.gender = gender;
    }
}
